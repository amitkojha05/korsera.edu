import { Schema } from "mongoose";
import mongoose from "mongoose";

const UserSchema = new Schema({
  name: {
    type: String,
    required: true,
  },
  email: {
    type: String,
    required: true,
    unique: true,
  },
  password: {
    type: String,
    required: true,
  },
  role: {
    type: String,
    required: true,
  },
});

const AuthUser =
  mongoose.models.AuthUser || mongoose.model("AuthUser", UserSchema);

export default AuthUser;
